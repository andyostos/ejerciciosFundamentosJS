 /** Declara una funcion para cada problema planteado*/
 /** calcular el perimetro de un circulo*/
 function perimetroCirculo(l){
        p = l * 3.1416;
        return p;
 }
 
 /** calcular el area de un rectangulo*/
 function areaRectangulo(a, b){
        x = a * b;
        return x;
 }
 /** calcular el cuadrado de un numero*/
 function cuadradoNumero(e){
        r = e * e;
        return r;
 }
 /** calcular la conversion de grados celsius a farenheit*/
 function celsiusFarenheit(c){
        f = (c * (9/5)) + 32;
        return f;
 }
 /** calcular el valor del voltaje dadas la resistencia y la corriente*/
 function voltaje(r, a){
        v = r * a;
        return v; 
 }
 /** calcular el volumen de una esfera*/
 function volumenEsfera(r){
        v = (4/3) * 3.1416 * (r * r * r) ;
        return v;
 }
 
